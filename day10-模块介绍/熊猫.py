#coding=utf-8
#panda
import turtle
t = turtle.Turtle()
# 头
def drawPanda():
    t.pendown()
    t.speed(0)
    t.pensize(5)
    t.penup()
    t.goto(100,85)
    t.pendown()
    t.left(80)
    t.circle(160,210)
    t.left(10)
    t.circle(150, 110)
#右前肢
    t.begin_fill()
    t.right(100)
    t.circle(55,190)
    t.right(-78)
    t.circle(500,15)
    t.end_fill()
#左前肢
    t.penup()
    t.goto(-180,25)
    t.begin_fill()
    t.pendown()
    t.right(30)
    t.circle(-450, 10)
    t.right(5)
    t.circle(-30,180)
    t.circle(-450,7.1)
    t.end_fill()
#左耳朵
    t.penup()
    t.goto(-180,215)
    t.pendown()
    t.left(180)
    t.begin_fill()
    t.circle(-35,282)
    t.end_fill()
#右耳朵
    t.penup()
    t.goto(55,225)
    t.pendown()
    t.left(180)
    t.begin_fill()
    t.circle(-35, 273)
    t.end_fill()
#左眼睛
    t.penup()
    t.goto(-160,180)
    t.pendown()
    t.fillcolor('black')
    t.begin_fill()
    t.right(100)
    t.circle(-50,90)
    t.right(30)
    t.circle(-50,150)
    t.right(30)
    t.circle(-50,60)
    t.end_fill()
#右眼睛
    t.penup()
    t.goto(30,190)
    t.pendown()
    t.fillcolor('black')
    t.begin_fill()
    t.right(100)
    t.circle(-50, 90)
    t.right(30)
    t.circle(-50, 150)
    t.right(30)
    t.circle(-50, 60)
    t.end_fill()
# 左眼白
    t.penup()
    t.goto(-100, 150)
    t.pendown()
    t.fillcolor('white')
    t.begin_fill()
    t.right(100)
    t.circle(-20, 90)
    t.right(30)
    t.circle(-20, 150)
    t.right(30)
    t.circle(-20, 60)
    t.end_fill()
#右眼白
    t.penup()
    t.goto(-10,142)
    t.pendown()
    t.fillcolor('white')
    t.begin_fill()
    t.right(100)
    t.circle(-18, 90)
    t.right(30)
    t.circle(-18, 150)
    t.right(30)
    t.circle(-18, 60)
    t.end_fill()
#鼻子
    t.penup()
    t.goto(-50,95)
    t.pendown()
    t.fillcolor('black')
    t.begin_fill()
    t.left(90)
    t.circle(-18, 90)
    t.right(30)
    t.circle(-18, 150)
    t.right(30)
    t.circle(-18, 60)
    t.end_fill()
#嘴巴
    t.penup()
    t.goto(-100,70)
    t.pendown()
    t.left(55)
    for i in range(2):
        t.circle(20,180)
        t.right(180)
#身体
    t.penup()
    t.goto(122,200)
    t.pendown()
    t.left(120)
    t.circle(-120,120)
    t.right(50)
    t.circle(-200,62)
#后肢
    t.penup()
    t.goto(270,75)
    t.pendown()
    t.begin_fill()
    t.circle(-20,100)
    t.right(20)
    t.circle(-100,30)
    t.right(40)
    t.circle(-30,120)
    t.right(20)
    t.circle(-80,30)
    t.end_fill()
#尾巴
    t.penup()
    t.goto(250,200)
    t.pendown()
    t.begin_fill()
    t.circle(-15)
    t.end_fill()

drawPanda()
turtle.done()